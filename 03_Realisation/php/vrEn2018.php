</p>
<h2>Les films en VR</h2></br>
<p>
  Les films en VR ont été “nombreux” cette année, entre autres nous avons:</p></br> 
<i>Crow: The Legend</i></br>
<i>Spheres: Songs of Spacetime</i></br>
<i>Ralph Breaks VR</i></br></br>
<p>
Pour l’instant ça peut ne pas paraître incroyable mais c’est un pas vers la
démocratisation de cette technologie, qui selon beaucoup de développeurs et de
scientifiques, sera implémentée dans notre quotidien dans un futur très proche.
</p></br>
<h2>La VR “location-based”</h2></br>
<p>
  Les salles dédiées à la VR n’ont cessé d’augmenter ces derniers temps avec la hype de la
VR. Les entreprises en profitent et elles ont raison, étant donné qu’il faut un PC puissant
et donc cher pour pouvoir supporter la VR à la maison. Ces derniers vous permettent
donc de faire l’expérience de la VR à des prix cassés comme aux États-Unis où l’on
retrouve des salles du genre à seulement 20$ par heure.
</p></br>
<h2>La VR autonome</h2></br>
<p>
  La VR autonome n’est autre que le fait d’avoir un casque qui n’est pas relié à un
ordinateur, en 2018 nous avons eu le droit à l’Oculus Go; le premier casque VR qui permet
à l’utilisateur de bouger librement.
Il embarque simplement des caméras à l'intérieur du casque pour pouvoir localiser la
personne dans la pièce dans laquelle elle se situe et permet de s’y connecter à distance.</br>
</p></br>
<h2>La VR pour le shopping ou l’entraînement</h2></br>
<p>
  La VR est aussi venue s’implémenter au shopping et à l’armée.
D’un côté nous retrouvons Walmart qui permet à ses clients de voir le catalogue en VR, et
de l’autre nous avons l’armée américaine qui permet à ses soldats de s’entraîner en
conditions presque réelles.
</p>
