<h1>La VR en 2018</h1>
<p>
  Si 2016 était la naissance des technologies de réalité virtuelle et augmentée, 2018 serait
sa maternelle. Même si depuis 2016 la technologie ne s’est pas crucialement améliorée
mais plutôt standardisée, l’année 2018 fût bien différente.</br>
Il y eut donc beaucoup de changements comme des sorties de casques, l’avancement
des softwares et bien d’autres changements ont consolidé les technologies d’immersion.</br>
</p>
