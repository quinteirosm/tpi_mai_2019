<h1>
  Définition
</h1>
<p>
  La réalité virtuelle (ou Virtual Reality en anglais) est une expression qui désigne les dispositifs permettant de simuler
  numériquement un environnement par la machine (ordinateur). Selon les technologies employées, elle permet à l’utilisateur
  de ressentir un univers virtuel par le biais de ses différent sens : la vue le plus souvent mais aussi le toucher, l’ouïe, l’odorat.
</p>
</br>
<p>
  La réalité virtuelle permet donc à une personne de vivre une expérience d’immersion et de mener une activité sensomotrice
  dans un monde artificiel. Pour garantir une immersion totale, l’utilisateur se sert d’un casque de réalité virtuelle.
  Celui-ci utilise le principe d’affichage en 3D stéréoscopique pour placer le visualisateur dans un monde virtuel généré par
  une machine.
</p>
