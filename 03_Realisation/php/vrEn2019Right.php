<h2>L’eye tracking, nouvelle fonctionnalité majeure des casques</h2></br>
<p>
  En 2019, on peut s’attendre à ce que de nombreux nouveaux casques VR proposent des fonctionnalités de suivi oculaire.
L’eye tracking permet notamment d’améliorer les performances graphiques grâce à la une technique qui imite la façon
dont notre oeil fonctionne au naturel en améliorant la netteté de l’endroit de l’image où l’utilisateur regarde. De même,
l’eye tracking peut permettre aux avatars d’imiter les mouvements d’yeux des utilisateurs, ou encore d’améliorer le
réalisme du focus et des effets de profondeur de champ.
Cette technologie est déjà proposée sur certains casques VR haut de gamme comme le StarVR One ou le VRGineers
XTAL, mais cela reste destiné à un public élitiste qui souhaite y mettre les moyens. En 2019, on peut s’attendre à ce que
l’eye tracking se démocratise et s’étende aux casques de milieu de gamme.
</p></br>
<h2>La VR coupe le cordon</h2></br>
<p>
  Les ventes des casques de première génération sont limitées par diverses caractéristiques techniques. Notamment le
matériel embarqué qui n’est pas pour l’instant de la qualité espérée ainsi que le fait d’être reliés à une machine par des
câbles. Mais ces problèmes pourraient être résolus en 2019 par l’arrivée des casques de seconde génération. Les
casques équipés d’un matériel embarqué plus poussé seront chers au début, mais les prix devraient vite chuter pour
pouvoir faire un sortes que la VR se démocratise plus vite.
</p></br>
<h2>Oculus et HTC détrônés sur le marché de la VR PC ?</h2></br>
<p>
  Depuis 2016, Oculus et HTC sont assis sur le trône de la VR PC grâce à leurs casques respectifs. Cependant, du temps
est passé depuis que ces pionniers ont sorti leur casque et de nouveaux constructeurs devraient annoncer les leurs qui
définiraient donc une concurrence directe.
</p></br>
<h2>Toujours plus de “VR location-based”</h2></br>
<p>
  Ce qu’on appelle la VR « location-based » consiste à proposer aux personnes de payer pour une expérience VR en dehors
de chez eux. Bien souvent ces expériences sont beaucoup plus spectaculaires étant donné qu’on est dans un lieu dédié
où l’on a plus de place que dans une chambre.
Par exemple, l’entreprise The Void a créé l’expérience « Star Wars : Secrets of Empire » qui s’est déroulée à l’échelle d’un
entrepôt et qui permettait aux utilisateurs de ressentir des stimulations sensorielles au travers de divers dispositifs. Face
aux ventes plus basses qu’espéré des casques, de nouveaux lieux proposant des expériences VR devraient ouvrir en
2019.
</p>
